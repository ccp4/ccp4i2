# Cache of import errors: task_name -> error message
_import_errors: dict[str, str] = {}


def _get_task_class(task_name: str, class_type: str):
    task = TASKS.get(task_name)
    if task is None:
        return None
    path = getattr(task, f"{class_type}Path")
    try:
        module_name, class_name = path.split(":")
        module = importlib.import_module(module_name)
        return getattr(module, class_name)
    except Exception as e:
        logger = logging.getLogger(f"ccp4i2:{__name__}")
        logger.error(f"Failed to import {class_type} for {task_name}: {e}")
        logger.error(f"Traceback:\n{traceback.format_exc()}")
        _import_errors[task_name] = str(e)
        return None


def get_import_error(task_name: str) -> str | None:
    """Return the import error message for a task, or None if it loaded OK or wasn't attempted."""
    return _import_errors.get(task_name)


@cache
def get_plugin_class(task_name: str):
    return _get_task_class(task_name, "plugin")


@cache
def get_report_class(task_name: str):
    return _get_task_class(task_name, "report")


def get_task_title(task_name: str):
    task = TASKS.get(task_name)
    if task is None:
        return None
    return task.shortTitle or task.title


def get_watched_file(task_name: str):
    task = TASKS.get(task_name)
    return task.watchedFile if task else None


def locate_def_xml(task_name: str):
    task = TASKS.get(task_name)
    if task is not None:
        rel_path = task.defXmlPath  # relative to I2_TOP
        if rel_path:
            abs_path = (I2_TOP / rel_path).resolve()
            if abs_path.exists():
                return abs_path
    return None


def supports_running_report(task_name: str):
    task = TASKS.get(task_name)
    return task.runningReport if task else False
