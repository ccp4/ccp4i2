const DEPRECATED_TASKS: Record<string, string> = {
  PrepareDeposit: "Superseded by adding_stats_to_mmcif_i2 — do not use",
};

type TaskInfo = {
  TASKTITLE?: string;
  DESCRIPTION?: string;
  shortTitle?: string;
  isAutogenerated: boolean;
};
type TaskRef = { name: string; info: TaskInfo };
type TaskLookup = Record<string, TaskInfo>;

function uncategorisedCategory(lookup: TaskLookup): TaskCategory {
  const assigned = new Set<string>();
  TASK_CATEGORIES.forEach((category) => {
    category.tasks.forEach((task) => assigned.add(task));
  });
  return {
    icon: "ccp4i2",
    title: "Uncategorised (for developer use only)",
    tasks: Object.keys(lookup)
      .filter((t) => !assigned.has(t))
      .sort((a, b) => {
        const titleA = (lookup[a]?.TASKTITLE ?? a).toLowerCase();
        const titleB = (lookup[b]?.TASKTITLE ?? b).toLowerCase();
        return titleA.localeCompare(titleB);
      }),
  };
}

export function TaskChooser(props: {
  onTaskSelect: (taskName: string) => void;
}) {
  const api = useApi();
  const [searchText, setSearchText] = useState<string>("");
  const [viewMode, setViewMode] = useState<ViewMode>("list");
  const { data: taskLookup } = api.get<TaskLookup>("task_lookup/");

  return (
    <Stack sx={{ height: "100%" }}>
      <Stack direction="row" alignItems="center" spacing={2} sx={{ p: 2 }}>
        {taskLookup && (
          <Typography>Tasks&nbsp;({Object.keys(taskLookup).length})</Typography>
        )}
        <SearchField
          value={searchText}
          onChange={setSearchText}
          placeholder="Search tasks..."
          size="small"
        />
        <ViewModeToggle mode={viewMode} onChange={setViewMode} />
      </Stack>
      <Box
        sx={{ flex: "auto", overflowY: "auto", p: 1, scrollbarWidth: "thin" }}
      >
        {taskLookup &&
          (searchText.trim().length > 0 ? (
            <FilteredTasks
              taskLookup={taskLookup}
              searchText={searchText}
              viewMode={viewMode}
              onTaskSelect={props.onTaskSelect}
            />
          ) : (
            <TaskTree
              taskLookup={taskLookup}
              viewMode={viewMode}
              onTaskSelect={props.onTaskSelect}
            />
          ))}
      </Box>
    </Stack>
  );
}

function FilteredTasks(props: {
  taskLookup: TaskLookup;
  searchText: string;
  viewMode: ViewMode;
  onTaskSelect: (taskName: string) => void;
}) {
  const filteredTasks = useMemo(() => {
    const searchText = props.searchText.toUpperCase();
    return Object.keys(props.taskLookup).filter((name) => {
      if (name.toUpperCase().includes(searchText)) return true;
      const info = props.taskLookup[name];
      return (
        info.TASKTITLE?.toUpperCase().includes(searchText) ||
        info.DESCRIPTION?.toUpperCase().includes(searchText)
      );
    });
  }, [props.searchText, props.taskLookup]);

  return (
    <TaskItems
      taskLookup={props.taskLookup}
      taskNames={filteredTasks}
      viewMode={props.viewMode}
      onTaskSelect={props.onTaskSelect}
    />
  );
}

function TaskTree(props: {
  taskLookup: TaskLookup;
  viewMode: ViewMode;
  onTaskSelect: (taskName: string) => void;
}) {
  const uncategorised = useMemo(
    () => uncategorisedCategory(props.taskLookup),
    [props.taskLookup],
  );

  return (
    <>
      {TASK_CATEGORIES.map((category, index) => (
        <TaskTreeFolder
          key={index}
          category={category}
          taskLookup={props.taskLookup}
          viewMode={props.viewMode}
          onTaskSelect={props.onTaskSelect}
        />
      ))}
      <TaskTreeFolder
        category={uncategorised}
        taskLookup={props.taskLookup}
        viewMode={props.viewMode}
        onTaskSelect={props.onTaskSelect}
      />
    </>
  );
}

function TaskTreeFolder(props: {
  category: TaskCategory;
  taskLookup: TaskLookup;
  viewMode: ViewMode;
  onTaskSelect: (taskName: string) => void;
}) {
  return (
    <Accordion disableGutters>
      <AccordionSummary expandIcon={<ExpandMoreIcon />}>
        <Stack direction="row" alignItems="center" spacing={2}>
          <Avatar src={`/svgicons/${props.category.icon}.svg`} alt="" />
          <Typography variant="subtitle1" sx={{ fontWeight: 500 }}>
            {props.category.title}
          </Typography>
        </Stack>
      </AccordionSummary>
      <AccordionDetails>
        <TaskItems
          taskLookup={props.taskLookup}
          taskNames={props.category.tasks}
          viewMode={props.viewMode}
          onTaskSelect={props.onTaskSelect}
        />
      </AccordionDetails>
    </Accordion>
  );
}

function TaskItems(props: {
  taskLookup: TaskLookup;
  taskNames: string[];
  viewMode: ViewMode;
  onTaskSelect: (taskName: string) => void;
}) {
  const tasks = useMemo<TaskRef[]>(
    () =>
      props.taskNames.flatMap((name) => {
        const info = props.taskLookup[name];
        return info ? [{ name, info }] : [];
      }),
    [props.taskLookup, props.taskNames],
  );

  return props.viewMode === "cards" ? (
    <TaskCards tasks={tasks} onTaskSelect={props.onTaskSelect} />
  ) : (
    <TaskList tasks={tasks} onTaskSelect={props.onTaskSelect} />
  );
}

function TaskList(props: {
  tasks: TaskRef[];
  onTaskSelect: (taskName: string) => void;
}) {
  return (
    <List>
      {props.tasks.map(({ name, info }) => {
        return (
          <ListItemButton key={name} onClick={() => props.onTaskSelect(name)}>
            <ListItemAvatar>
              <Avatar src={`/svgicons/${name}.svg`} alt="" />
            </ListItemAvatar>
            <ListItemText
              primary={info.TASKTITLE || name}
              secondary={info.DESCRIPTION || ""}
            />
          </ListItemButton>
        );
      })}
    </List>
  );
}

function TaskCards(props: {
  tasks: TaskRef[];
  onTaskSelect: (taskName: string) => void;
}) {
  return (
    <Box
      sx={{
        display: "grid",
        gridTemplateColumns: "repeat(auto-fill, minmax(min(280px, 100%), 1fr))",
        gap: 2,
        p: 1,
      }}
    >
      {props.tasks.map(({ name, info }) => (
        <TaskCard
          key={name}
          taskName={name}
          taskInfo={info}
          onTaskSelect={props.onTaskSelect}
        />
      ))}
    </Box>
  );
}

function TaskCard(props: {
  taskName: string;
  taskInfo: TaskInfo;
  onTaskSelect: (taskName: string) => void;
}) {
  return (
    <Card
      sx={{
        minHeight: "14rem",
        minWidth: "280px",
        ":hover": { boxShadow: 24, cursor: "pointer" },
      }}
      onClick={() => props.onTaskSelect(props.taskName)}
    >
      <CardHeader
        slotProps={{ title: { variant: "button", my: 0, py: 0 } }}
        title={
          <>
            <Avatar src={`/svgicons/${props.taskName}.svg`} alt="">
              {props.taskName?.[0]?.toUpperCase()}
            </Avatar>
            {props.taskInfo.shortTitle || props.taskInfo.TASKTITLE || props.taskName}
          </>
        }
        subheader={props.taskInfo.TASKTITLE || props.taskName}
      />
      <CardContent>
        {DEPRECATED_TASKS[props.taskName] && (
          <Chip
            label={DEPRECATED_TASKS[props.taskName]}
            size="small"
            color="error"
            variant="filled"
            sx={{ mb: 1, fontSize: "0.7rem" }}
          />
        )}
        {props.taskInfo.isAutogenerated && (
          <Chip
            label="Auto-generated Interface"
            size="small"
            color="info"
            variant="outlined"
            sx={{ mb: 1, fontSize: "0.7rem" }}
          />
        )}
        <p>{`${props.taskInfo.DESCRIPTION || ""}`}</p>
      </CardContent>
    </Card>
  );
}
